CREATE OR REPLACE VIEW cc_qui_a_joue_quoi AS
   SELECT com_nom, com_debut, spe_nom, COUNT(jou_rep_no) "NB_JOUE"
      FROM cc_comedien JOIN cc_joue ON jou_com_no=com_no JOIN cc_representation ON rep_no=jou_rep_no JOIN cc_spectacle ON spe_no=rep_spe_no
      WHERE rep_date < '20/10/2022'
      GROUP BY com_nom, com_debut, spe_nom
      ORDER BY com_nom;

